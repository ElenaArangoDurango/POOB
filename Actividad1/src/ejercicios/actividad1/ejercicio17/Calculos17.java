public class Calculos17 {
    static double calcular_area (double radio){
        double area = Math.PI*Math.pow(radio,2);
        return area;
    }
    static double calcular_perimetro (double radio){
        double perimetro = Math.PI*radio*2;
        return perimetro;
    }
    
}


